type IconOptions =
  | "department"
  | "easy"
  | "exact"
  | "average"
  | "history"
  | "instructor"
  | "about";

function getIcon(iconName: IconOptions) {
  if (iconName === "department") return <DepartmentIcon />;
  else if (iconName === "easy") return <EasyIcon />;
  else if (iconName === "exact") return <ExactIcon />;
  else if (iconName === "average") return <AverageIcon />;
  else if (iconName === "history") return <HistoryIcon />;
  else if (iconName === "instructor") return <InstructorIcon />;
  else if (iconName === "about") return <AboutIcon />;
  else return null;
}

